/*
Problem:
Write a Java program to print the sum (addition), multiply, subtract, divide and remainder of two numbers.

*/
class programming3
{
    public static void main(String args[])
    {
         int a =5;
         int b =6;

        System.out.println("The sum of the number is"+a+b);
        System.out.println("The sub of the number is"+a-b);
        System.out.println("The multiple of the number is"+a*b);
        System.out.println("The division of the number is"+a/b);
        System.out.println("The remainder of the number is"+a%b);

    }
}



